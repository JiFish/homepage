<h1>Geef & JiFish Dig To Bedrock</h1>
<p>Me and Geef for some reason decided to dig an insane hole to bedrock and make it into an awesome base. This is a 100% legit SMP build. No mods or commands.</p>
<p style="font-family:sans-serif">Historical Note: This build was completed in September 2011. Minecraft has come a long way since then, and this build is less impressive by modern standards. I think it is worth noting that in the <a href="http://minecraft.gamepedia.com/Beta_1.7">version we were playing in</a>, there was no silk touch or efficiency enchantment - or enchantments of any kind. It also lacked beacons and potions. Conversely, there was no hunger mechanic. The fishcraft server has been around for 5 years, but this is still one of my favourite builds.</p>
<p>You can see the whole map here: <a href="http://fishcraft.jifish.co.uk/map">Fishcraft Map</a>.</p>
